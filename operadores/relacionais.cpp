#include <iostream>

using namespace std;

int main(int argc, char const *argv[]) {
    
    int a = 5;
    int b = 10;

    //igual ==
    cout << (a == b) << endl;

    //diferente !=
    cout << (a != b) << endl;

    //maior que > ou menor que <
    cout << (a > b) << endl;
    cout << (a < b) << endl;

    //maior ou igual >= e menor ou igual <=
    cout << (a >= b) << endl;
    cout << (a <= b) << endl;
    

    return 0;
}
